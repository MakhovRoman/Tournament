function getCurrenArray(data) {
    let result;

    for (let key in data) {
        result = data[key];
    }

    for (let prop of result) {
        for (let item in prop) {
            if ( !isNaN(prop[item]) ) prop[item] = Number(prop[item]);
        };
    }

    return result;
};

