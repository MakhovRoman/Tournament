function getCategories(data, weight, ageMin, ageMax, gender) {
    let result = [];

    for (let item of data) {
        if (item.weight <= weight && item.gender == gender && (item.age == ageMin || item.age == ageMax) ) result.push(item);
    }

    return result;
}

